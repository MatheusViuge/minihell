#include "libft.h"

char	*ft_strchr(char const *s, int c)
{
	int	i;
	int	len;

	if (!s || !*s)
		return (NULL);
	len = (int)ft_strlen(s);
	i = 0;
	while (i <= len)
	{
		if (s[i] == (char)c)
			return ((char *)&s[i]);
		i++;
	}
	return (NULL);
}
